type SearchSelectOption = {
  value: string
  label: string
  icon?: React.ReactNode
}

type SearchSelectProps = {
  options: SearchSelectOption[]
  placeholder?: string
  searchPlaceholder?: string
  value?: string
  onChange?: (value: string) => void
  buttonClassName?: string
  contentClassName?: string
  disabled?: boolean
  extraOptions?: React.ReactNode
  side?: "top" | "bottom" | "left" | "right"
  align?: "start" | "center" | "end"
  onlyIcon?: boolean
  label?: string
  inputProps?: React.ComponentProps<typeof CommandInput>
}

function SearchSelect({
  options,
  placeholder = "Select option...",
  searchPlaceholder = "Search...",
  value,
  onChange,
  buttonClassName,
  contentClassName,
  disabled = false,
  extraOptions,
  side = "bottom",
  align = "start",
  onlyIcon = false,
  label,
  inputProps,
}: SearchSelectProps) {
  const [open, setOpen] = React.useState(false);
  const [internalValue, setInternalValue] = React.useState<string>("");

  // Use controlled or uncontrolled value
  const selectedValue = value != null ? value : internalValue;

  // Filter options based on search
  const [search, setSearch] = React.useState("");
  const filteredOptions = React.useMemo(
    () =>
      options.filter((option) =>
        option.label.toLowerCase().includes(search.toLowerCase())
      ),
    [options, search]
  );

  const handleSelect = (currentValue: string) => {
    if (value === undefined) {
      setInternalValue(currentValue === selectedValue ? "" : currentValue);
    }
    onChange?.(currentValue === selectedValue ? "" : currentValue);
    setOpen(false);
  };

  const selectedOption =
    options.find((option) => option.value === selectedValue);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className={cn(
            "w-full justify-between overflow-hidden", // Add overflow-hidden
            buttonClassName,
            {
              "has-[>svg]:px-2 px-2": onlyIcon,
            }
          )}
          disabled={disabled}
        >
          <span
            className={cn(
              "flex items-center gap-2",
              {
                "text-muted-foreground": !selectedOption && !!placeholder,
                "min-w-0 truncate": !onlyIcon,
              }
            )}
          >
            {selectedOption ? (
              <>
                {onlyIcon ? selectedOption.icon : (
                  <>
                    {selectedOption.icon}
                    <span className="truncate">{selectedOption.label}</span>
                  </>
                )}
              </>
            ) : (
              <span className="truncate">{placeholder}</span>
            )}
          </span>
          <ChevronsUpDownIcon className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className={cn("p-0", contentClassName)} side={side} align={align}>
        <Command>
          <CommandInput
            placeholder={searchPlaceholder}
            value={search}
            onValueChange={setSearch}
            {...inputProps}
          />
          <CommandList>
            <CommandEmpty>No {label?.toLowerCase() ?? "option"} found.</CommandEmpty>
            <CommandGroup>
              {filteredOptions.map((option) => (
                <CommandItem
                  key={option.value}
                  value={option.value}
                  onSelect={(currentValue) => handleSelect(currentValue)}
                  className="break-all"
                >
                  <CheckIcon
                    className={cn(
                      "mr-2 h-4 w-4",
                      selectedValue === option.value
                        ? "opacity-100"
                        : "opacity-0"
                    )}
                  />
                  {option.icon}
                  {option.label}
                </CommandItem>
              ))}
              {extraOptions}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  );
}

